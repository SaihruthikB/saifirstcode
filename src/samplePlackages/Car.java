package samplePlackages;

public class Car {

	private int Model;
	private String Driver;
	private int speed;
	
	public int getModel() {
		return Model;
	}
	public void setModel(int model) {
		this.Model = model;
	}
	public String getDriver() {
		return Driver;
	}
	public void setDriver(String driver) {
		this.Driver = driver;
	}
	public int getSpeed() {
		return speed;
	}
	public void setSpeed(int speed) {
		this.speed = speed;
	}

}
